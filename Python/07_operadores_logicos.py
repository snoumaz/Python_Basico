"""
Operadores logicos 
Sirven para poder combinar diversas condiciones
"""

# and -> significa y => JS -> &&
print("True and True ->", True and True) #True = 1
print("True and False ->", True and False) #False = 0
print("False and False ->", False and False) #False = 0

# or -> significa o => JS ||
print("True or True ->", True or True) #True = 1
print("True or False ->", True or False) #False = 1
print("False or True ->", False or True) #False = 1
print("False or False ->", False and False) #False = 0

